    #include <iostream>

    using namespace std;

    int main() {
        //first way with if statement
        double value1, value2, largerValue;

        cout << "Please enter a number : ";
        cin >> value1;
        cout << "Please enter a second number : ";
        cin >> value2;

        if (value1 > value2) {
            largerValue = value1;
        } else {
            largerValue = value2;
        }

        cout << "The larger value is " << largerValue << endl;

        //second way with conditional operator

        double value3, value4, largerValue2;
        cout << "Please enter a number : ";
        cin >> value3;
        cout << "Please enter a second number : ";
        cin >> value4;

        largerValue2 = (value3 > value4) ? value3 : value4;

        cout << "The larger value is " << largerValue2 << endl;

        return 0;
    }
